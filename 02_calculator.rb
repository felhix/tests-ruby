def add (n1, n2)
  number = n1 + n2
  return number
end

def subtract (n1, n2)
  number = n1 - n2
  return number
end

def sum (n_array)
  number = sum(n_array)
  return number
end

def multiply (n1, n2)
  number = n1 * n2
  return number
end

def power (n1, n2)
  number = n1 ** n2
  return number
end

def factorial (n)
  return (1..n).inject(:*) || 1
end